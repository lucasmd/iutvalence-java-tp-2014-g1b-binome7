/**
 * Création de la classe pour énumération pour les constantes des orientations
 * possible (sens : avant, arrière / direction : horizontale, verticale)
 */

public enum EnumOrientation
{
	/**
	 * Déclaration de la constante pour arrière
	 */
	HAUT,
	/**
	 * Déclaration de la constante pour avant
	 */
	BAS,
	/**
	 * Déclaration de la constante pour horizontale
	 */
	GAUCHE,

	/**
	 * Déclaration de la constante pour verticale
	 */
	DROITE,
	/**
	 * Déclaration de la constante pour verticale
	 */
	HAUT_GAUCHE,
	/**
	 * Déclaration de la constante pour verticale
	 */
	BAS_DROITE,
	/**
	 * Déclaration de la constante pour verticale
	 */
	HAUT_DROITE,
	/**
	 * Déclaration de la constante pour verticale
	 */
	BAS_GAUCHE;
}