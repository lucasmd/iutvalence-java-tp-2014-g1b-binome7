package fr.iutvalence.java.tp.Puissance4;

/**
 * Création de la classe pour énumération pour les constantes des orientations possible
 * (sens : avant, arrière / direction : horizontale, verticale)
 */

public enum EnumOrientation
{
	/**
	 * Déclaration de la constante pour arrière
	 */
	ARRIERE,
	/**
	 * Déclaration de la constante pour avant
	 */
	AVANT,
	/**
	 * Déclaration de la constante pour horizontale
	 */
	HORIZONTALE,

	/**
	 * Déclaration de la constante pour verticale
	 */
	VERTICALE;
}